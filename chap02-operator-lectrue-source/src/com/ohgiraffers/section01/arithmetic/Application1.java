package com.ohgiraffers.section01.arithmetic;

public class Application1 {
    public static void main(String[] args) {
        /* 산술 연산자에 대해 이해하고 솰용할 수 있다. */
        int num1 = 20;
        int num2 = 7;
        System.out.println("num1 + num2 = " + (num1 + num2));
        System.out.println("num1 - num2 = " + (num1 - num2));
        System.out.println("num1 * num2 = " + (num1 * num2));
        System.out.println("num1 / num2 = " + (num1 / num2));
        System.out.println("num1 % num2 = " + (num1 % num2));

    }
}
